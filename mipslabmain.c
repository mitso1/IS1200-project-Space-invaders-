void convGraph(){
	int block = 0;
	int i;
	for (i = 0; i < 512; i++)
	{
		if (i % 128 == 0 && i != 0)
		{
			block += 128*7;
		}
		
		graphics[i] = 0; // (calcGraph[i] | calcGraph[(i+1) * 128] << 1)
		int  j;
		for (j = 0; j < 8; j++)
		{
			graphics[i] |= calcGraph[((i + block) + (j * 128))] << j;
		}
		
	}
	
}


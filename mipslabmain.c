int convPosition(uint16_t pos){
	int temp = 0;
	int i;
	for (i = 0; i < 32; i++)
	{
		int j;
		for (j = 0; j < 128; j++)
		{
			if ((int) pos == j + i*128)
			{
				temp = j + ((i/8) * 128);
			}	
		}
	}
	return (int)temp;
}
uint8_t calcOffset(uint16_t pos){
	int i;
	for (i = 0; i < 32; i++)
	{
		int j;
		for (j = 0; j < 128; j++)
		{
			if (pos == j + i*128)
			{
				return (uint8_t) i % 8;
			}	
		}
	}

}


